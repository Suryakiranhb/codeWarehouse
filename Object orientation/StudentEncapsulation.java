package com.practice.methods.encapsulation;

public class StudentEncapsulation {
	private String name;
	private int age;
	private int rollno;
	//name
	public void setName(String names) {
		name = names;
	}
	public String getName() {
		return name;
	}
	//age
	public void setAge(int ages) {
		age = ages;
	}
	public int getAge() {
		return age;
	}
	//roll number
	public void setRoll(int roll) {
		rollno = roll;
	}
	public int getRoll() {
		return rollno;
	}
	
}
